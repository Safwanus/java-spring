package main.java.model;

public class Status {
	private int id;
	private int eid;
	private String option;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getEId() {
		return eid;
	}

	public void setEId(int eid) {
		this.eid = eid;
	}

	public String getOption() {
		return option;
	}

	public void setOption(String option) {
		this.option = option;
	}

}
